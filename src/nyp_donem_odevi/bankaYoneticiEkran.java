    private void p3_ekle_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_p3_ekle_buttonActionPerformed
        // TODO add your handling code here:
        String ad = p3_ad.getText();
        String soyad = p3_soyad.getText();
        String sifre = p3_sifre.getText();
        String telefon = p3_telefon.getText();
        String tc = p3_tc.getText();
        String cinsiyet = p3_cinsiyet.getSelectedItem().toString();
        if (ad.equals("") || soyad.equals("") || sifre.equals("") || telefon.equals("") || tc.equals("") || cinsiyet.equals("")) {
            JOptionPane.showMessageDialog(null, "Yıldızlı alanlar boş bırakılamaz!");
        } else {
            try {
                Musteri musteri = new Musteri(ad, soyad, sifre, telefon, tc, cinsiyet);
                musteri.musteriKaydet();
            } catch (IOException ex) {
                Logger.getLogger(PersonelEkran.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_p3_ekle_buttonActionPerformed

