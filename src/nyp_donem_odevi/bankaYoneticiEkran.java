    private void dialog_guncelle_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dialog_guncelle_buttonActionPerformed

        DefaultTableModel model = (DefaultTableModel) p1_musteri_table.getModel();
        int selected_row_index = p1_musteri_table.getSelectedRow();
        String musteri_id = model.getValueAt(selected_row_index, 0).toString();
        String tc = model.getValueAt(selected_row_index, 4).toString();
        String cinsiyet = model.getValueAt(selected_row_index, 5).toString();
        if (dialog_ad.equals("") || dialog_soyad.equals("") || dialog_telefon.equals("") || dialog_sifre.equals("")) {
            JOptionPane.showMessageDialog(null, "Yıldızlı alanlar boş bırakılamaz!");
        } else {
            // TODO Auto-generated method stub
            this.b.musteriBilgiGuncelle(musteri_id, dialog_ad.getText(), dialog_soyad.getText(), dialog_sifre.getText(), dialog_telefon.getText(), tc, cinsiyet);
            JOptionPane.showMessageDialog(null, "Müşteri başarıyla güncellendi!");
            musteri_guncelle.setVisible(false);
            p1_musteri_table_doldur();
        }
    }//GEN-LAST:event_dialog_guncelle_buttonActionPerformed

