    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            double tc = Double.parseDouble(dialog_tc.getText());

            if (dialog_sifre.getText().equals(dialog_sifre_tekrar.getText())) {

                String ad = dialog_ad.getText();
                String soyad = dialog_soyad.getText();
                String telefon = dialog_telefon.getText();
                String cinsiyet = dialog_cinsiyet.getSelectedItem().toString();
                String sifre = dialog_sifre.getText();

                Musteri m;
                try {
                    m = new Musteri(ad, soyad, sifre, telefon,dialog_tc.getText(), cinsiyet);
                    m.musteriKaydet();
                } catch (IOException ex) {
                    Logger.getLogger(GirisEkrani.class.getName()).log(Level.SEVERE, null, ex);
                }

            } else {
                JOptionPane.showMessageDialog(null, "Girdiğiniz şifreler uyuşmuyor!");
            }

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Lütfen geçerli bir tc kimlik giriniz!");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

