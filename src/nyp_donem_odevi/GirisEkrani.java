    private void personel_giris_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_personel_giris_buttonActionPerformed
        // TODO add your handling code here:
        Scanner reader;
        int kontrol = 0;
        try {
            reader = new Scanner(new BufferedReader(new FileReader("banka_personeli.txt")));
            while (reader.hasNextLine()) {
                String s = reader.nextLine();
                String[] bilgiler = s.split(",");

                if (bilgiler[0].equals(personel_id.getText()) && bilgiler[4].equals(personel_sifre.getText()) && bilgiler[10].equals("1") && bilgiler[1].equals("PERSONEL")) {
                    kontrol = 1;
                    BankaCalisan b = new BankaCalisan(Integer.parseInt(bilgiler[0]));
                    PersonelEkran p = new PersonelEkran(b);
                    girisKontrol(b);
                    p.setVisible(true);
                    this.setVisible(false);
                }

                if (bilgiler[0].equals(personel_id.getText()) && bilgiler[4].equals(personel_sifre.getText()) && bilgiler[10].equals("1") && bilgiler[1].equals("YONETICI")) {
                    kontrol = 1;
                    BankaYonetici b = new BankaYonetici(Integer.parseInt(bilgiler[0]));
                    bankaYoneticiEkran p = new bankaYoneticiEkran(b);
                    p.setVisible(true);
                    girisKontrol(b);
                    this.setVisible(false);
                }

            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(GirisEkrani.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if(kontrol == 0){
            JOptionPane.showMessageDialog(null, "Girdiğiniz bilgilerde personel bulunmamakta!");
        }
    }//GEN-LAST:event_personel_giris_buttonActionPerformed

    private void kullanici_giris_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kullanici_giris_buttonActionPerformed
        // TODO add your handling code here:
        Scanner reader;
        int kontrol = 0;
        try {
            reader = new Scanner(new BufferedReader(new FileReader("musteri.txt")));
            while (reader.hasNextLine()) {
                String s = reader.nextLine();
                String[] bilgiler = s.split(",");
                if (bilgiler[5].equals(kullanici_tc.getText()) && bilgiler[3].equals(kullanici_sifre.getText()) && bilgiler[7].equals("1")) {
                    kontrol = 1;
                    Musteri m = new Musteri(Integer.parseInt(bilgiler[0]));
                    musteriEkran d = new musteriEkran(m);
                    girisKontrol(m);
                    d.setVisible(true);
                    this.setVisible(false);
                }
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(GirisEkrani.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(kontrol == 0){
            JOptionPane.showMessageDialog(null, "Girdiğiniz bilgilerde müşteri bulunmamakta!");
        }
    }//GEN-LAST:event_kullanici_giris_buttonActionPerformed

