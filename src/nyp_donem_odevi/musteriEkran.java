    private void p12_onay_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_p12_onay_buttonActionPerformed
        // TODO add your handling code here:

        DefaultTableModel model1 = (DefaultTableModel) p12_hesap_tablo.getModel();
        DefaultTableModel model2 = (DefaultTableModel) p12_kredi_tablo.getModel();

        int selected_row_model1 = p12_hesap_tablo.getSelectedRow();
        int selected_row_model2 = p12_kredi_tablo.getSelectedRow();

        if (selected_row_model1 != -1 && selected_row_model2 != -1) {
            try {
                int yatirilacak_miktar = Integer.parseInt(p12_miktar.getText());
                int bakiye = Integer.parseInt(model1.getValueAt(selected_row_model1, 4).toString());
                float borc = Float.parseFloat(model2.getValueAt(selected_row_model2, 4).toString());

                if (yatirilacak_miktar <= bakiye) {

                    if (yatirilacak_miktar <= borc) {
                        int kredi_id = Integer.parseInt(model2.getValueAt(selected_row_model2, 0).toString());
                        int hesap_id = Integer.parseInt(model1.getValueAt(selected_row_model1, 0).toString());

                        try {
                            this.m.krediBorcOdeme(String.valueOf(kredi_id), String.valueOf(hesap_id), yatirilacak_miktar);
                        } catch (FileNotFoundException ex) {
                            Logger.getLogger(musteriEkran.class.getName()).log(Level.SEVERE, null, ex);
                        }

                        p12_tablo_doldur();
                        p12_tablo_doldur_kredi();
                    } else {
                        JOptionPane.showMessageDialog(null, "Mevcut Bor√ßtan Fazla √ñdeme Yapamazsƒ±nƒ±z!");
                    }

                } else {

                    JOptionPane.showMessageDialog(null, "Yetersiz Bakiye!");
                }

            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "L√ºtfen Tam Sayƒ± Giriniz!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "L√ºtfen bir hesap ve kredi se√ßiniz!");
        }

    }//GEN-LAST:event_p12_onay_buttonActionPerformed

